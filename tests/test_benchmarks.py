class TestFormatReport:
    def test_renders_human_readable_summary(self, tmp_path: Path) -> None:
        from dendra.benchmarks import aggregate_report, format_report

        runtime_root = tmp_path / "runtime"
        _write_bench_line(
            runtime_root,
            "triage_ticket",
            timestamp="2026-05-01T00:00:00+00:00",
            phase="RULE",
            cost_low=0.0042,
            cost_high=0.0042,
        )
        _write_bench_line(
            runtime_root,
            "triage_ticket",
            timestamp="2026-05-04T00:00:00+00:00",
            phase="MODEL_PRIMARY",
            cost_low=0.00031,
            cost_high=0.00031,
        )
        _write_bench_line(
            runtime_root,
            "route_intent",
            timestamp="2026-05-01T00:00:00+00:00",
            phase="RULE",
            cost_low=0.0058,
            cost_high=0.0058,
        )

        report = aggregate_report(runtime_root)
        text = format_report(report)
        # Headline: number of switches.
        assert "2 switches" in text
        # Per-switch lines.
        assert "triage_ticket" in text
        assert "route_intent" in text
        # Graduation event.
        assert "RULE" in text and "MODEL_PRIMARY" in text
        # Percent-change shows up for the graduating switch.
        assert "-92%" in text or "-92.0%" in text
        # No-graduation switch shows the no-baseline language.
        assert "no graduation yet" in text.lower()
        # No em-dashes anywhere in user-facing copy.
        assert "—" not in text


