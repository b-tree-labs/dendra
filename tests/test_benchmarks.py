class TestInitWithBenchmarksFlag:
    """``dendra init --auto-lift --with-benchmarks`` writes the bench
    stub alongside the lifted Switch module.
    """

    def test_init_with_benchmarks_emits_bench_stub(self, tmp_path: Path) -> None:
        from dendra.cli import main

        # A simple liftable function.
        target = tmp_path / "triage.py"
        target.write_text(
            "def triage(text):\n"
            "    if text.startswith('urgent'):\n"
            "        return 'high'\n"
            "    return 'low'\n"
        )
        rc = main(
            [
                "init",
                f"{target}:triage",
                "--author",
                "@bench-test:local",
                "--auto-lift",
                "--with-benchmarks",
            ]
        )
        assert rc == 0
        gen_dir = tmp_path / "__dendra_generated__"
        assert (gen_dir / "triage__triage.py").exists()
        bench_path = gen_dir / "triage__triage_bench.py"
        assert bench_path.exists(), (
            f"--with-benchmarks did not emit bench stub; "
            f"contents: {sorted(p.name for p in gen_dir.iterdir())}"
        )
        text = bench_path.read_text()
        # Drift-trackable header.
        assert "Generated by Dendra" in text
        assert "test_label_parity_across_phases" in text
