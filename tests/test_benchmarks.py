class TestRunBenchmark:
    """``run_benchmark`` executes the generated module and returns the
    JSONL line of headline metrics. We use a tiny synthetic Switch so
    the test stays hermetic (no network, no real pytest config).
    """

    def test_produces_jsonl_line_with_expected_fields(
        self, tmp_path: Path, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        from dendra.benchmarks import run_benchmark

        # Synthetic Switch defined as a real importable module.
        pkg_dir = tmp_path / "synthbench_pkg"
        pkg_dir.mkdir()
        (pkg_dir / "__init__.py").write_text("")
        (pkg_dir / "synth_switch.py").write_text(
            "from dendra import Switch\n"
            "\n"
            "class SynthSwitch(Switch):\n"
            "    def _evidence_text(self, text: str) -> str:\n"
            "        return text\n"
            "\n"
            "    def _rule(self, evidence) -> str:\n"
            "        return 'a' if evidence.text.startswith('a') else 'b'\n"
            "\n"
            "    def _on_a(self, text):\n"
            "        return None\n"
            "\n"
            "    def _on_b(self, text):\n"
            "        return None\n"
        )

        runtime_root = tmp_path / "runtime"
        monkeypatch.syspath_prepend(str(tmp_path))

        result = run_benchmark(
            switch_name="synth",
            switch_module="synthbench_pkg.synth_switch",
            switch_class_name="SynthSwitch",
            inputs=["alpha", "beta", "apex"],
            runtime_dir=runtime_root,
        )

        # Persisted file exists.
        bench_dir = runtime_root / "dendra" / "synth" / "benchmarks"
        files = sorted(bench_dir.glob("*.jsonl"))
        assert len(files) == 1
        line = files[0].read_text().strip()
        record = json.loads(line)
        for key in (
            "timestamp",
            "switch_name",
            "phase",
            "n_inputs",
            "label_parity",
            "latency_p50_ms",
            "latency_p95_ms",
            "cost_per_call_usd_low",
            "cost_per_call_usd_high",
            "estimated",
        ):
            assert key in record, f"missing field {key!r}"
        # Estimated cost (default mode) — never zero, never negative.
        assert record["cost_per_call_usd_low"] > 0
        assert record["cost_per_call_usd_high"] >= record["cost_per_call_usd_low"]
        assert record["estimated"] is True
        # The runner returns the same record it wrote.
        assert result["switch_name"] == "synth"
        assert result["n_inputs"] == 3


