"""TDD spec for the close-the-loop benchmark + report harness (v1.1).

Four behaviours we pin:

1. ``generate_benchmark_module`` writes a parseable Python file with
   the three pytest functions (parity / latency / cost) and the
   drift-trackable Dendra header.
2. ``run_benchmark`` executes the generated module via pytest and
   produces one JSONL line per run with the expected fields.
3. ``aggregate_report`` walks ``runtime/dendra/<switch>/benchmarks/``,
   identifies graduation events from a stub timeseries, and surfaces
   per-switch cost deltas.
4. ``format_report`` renders the human-readable text expected by the
   ``dendra report`` CLI verb.

The benchmark module collides with the existing ``dendra.benchmarks``
package (which holds the public-dataset loaders), so the harness lives
at ``dendra.benchmarks.harness`` and is re-exported from the package
init for both ``from dendra.benchmarks import generate_benchmark_module``
and ``from dendra.benchmarks.harness import ...``.
"""

from __future__ import annotations

import ast
import json
from pathlib import Path

import pytest

# ----------------------------------------------------------------------
# 1. Codegen: generate_benchmark_module
# ----------------------------------------------------------------------


class TestGenerateBenchmarkModule:
    """The codegen path called from ``dendra init --with-benchmarks``."""

    def test_writes_parseable_file_with_three_pytest_functions(self, tmp_path: Path) -> None:
        from dendra.benchmarks import generate_benchmark_module

        out_path = tmp_path / "__dendra_generated__" / "triage__triage_bench.py"
        generate_benchmark_module(
            out_path=out_path,
            source_module="triage",
            source_function="triage",
            switch_class_name="TriageSwitch",
            switch_module="triage___dendra_generated__.triage__triage",
            source_ast_hash="0" * 32,
            dendra_version="0.2.0",
        )
        assert out_path.exists()
        text = out_path.read_text()
        # Drift-trackable header from refresh.write_generated_file.
        assert "Generated by Dendra" in text
        assert "Source: triage:triage" in text
        assert "AST hash:" in text
        assert "Content hash:" in text
        # Parses cleanly.
        tree = ast.parse(text)
        function_names = {node.name for node in tree.body if isinstance(node, ast.FunctionDef)}
        assert "test_label_parity_across_phases" in function_names
        assert "test_latency_baseline" in function_names
        assert "test_cost_estimate_per_call" in function_names

    def test_includes_imports_for_switch_and_pytest(self, tmp_path: Path) -> None:
        from dendra.benchmarks import generate_benchmark_module

        out_path = tmp_path / "__dendra_generated__" / "x__y_bench.py"
        generate_benchmark_module(
            out_path=out_path,
            source_module="x",
            source_function="y",
            switch_class_name="YSwitch",
            switch_module="x___dendra_generated__.x__y",
            source_ast_hash="a" * 32,
            dendra_version="0.2.0",
        )
        text = out_path.read_text()
        assert "import pytest" in text
        assert "YSwitch" in text


