# 3. Report aggregation
# ----------------------------------------------------------------------


def _write_bench_line(
    runtime_root: Path,
    switch: str,
    *,
    timestamp: str,
    phase: str,
    cost_low: float,
    cost_high: float,
) -> None:
    bench_dir = runtime_root / "dendra" / switch / "benchmarks"
    bench_dir.mkdir(parents=True, exist_ok=True)
    record = {
        "timestamp": timestamp,
        "switch_name": switch,
        "phase": phase,
        "n_inputs": 100,
        "label_parity": 1.0,
        "latency_p50_ms": 1.0,
        "latency_p95_ms": 2.0,
        "cost_per_call_usd_low": cost_low,
        "cost_per_call_usd_high": cost_high,
        "estimated": True,
    }
    # Fixed filename per timestamp so files sort by name.
    safe_ts = timestamp.replace(":", "-")
    out_path = bench_dir / f"{safe_ts}.jsonl"
    out_path.write_text(json.dumps(record) + "\n")


class TestAggregateReport:
    """Walk a stub runtime tree, extract graduation events + cost
    deltas, surface a structured report.
    """

    def test_identifies_graduation_event_and_cost_delta(self, tmp_path: Path) -> None:
        from dendra.benchmarks import aggregate_report

        runtime_root = tmp_path / "runtime"
        # triage_ticket: graduates from RULE -> MODEL_PRIMARY between
        # day 1 and day 5; cost drops from $0.0042 to $0.00031.
        _write_bench_line(
            runtime_root,
            "triage_ticket",
            timestamp="2026-05-01T00:00:00+00:00",
            phase="RULE",
            cost_low=0.0042,
            cost_high=0.0042,
        )
        _write_bench_line(
            runtime_root,
            "triage_ticket",
            timestamp="2026-05-04T00:00:00+00:00",
            phase="MODEL_PRIMARY",
            cost_low=0.00031,
            cost_high=0.00031,
        )
        # route_intent: stays at RULE.
        _write_bench_line(
            runtime_root,
            "route_intent",
            timestamp="2026-05-01T00:00:00+00:00",
            phase="RULE",
            cost_low=0.0058,
            cost_high=0.0058,
        )

        report = aggregate_report(runtime_root)
        assert report.n_switches == 2
        # Map switch_name -> SwitchTimeseries
        by_name = {s.switch_name: s for s in report.switches}
        triage = by_name["triage_ticket"]
        assert triage.first_phase == "RULE"
        assert triage.latest_phase == "MODEL_PRIMARY"
        assert triage.graduation_event is not None
        assert triage.graduation_event.from_phase == "RULE"
        assert triage.graduation_event.to_phase == "MODEL_PRIMARY"
        assert triage.graduation_event.timestamp.startswith("2026-05-04")
        # Cost delta: (0.00031 - 0.0042) / 0.0042 ≈ -92%.
        assert triage.cost_baseline_low == pytest.approx(0.0042)
        assert triage.cost_latest_low == pytest.approx(0.00031)
        assert triage.cost_pct_change_low == pytest.approx(
            (0.00031 - 0.0042) / 0.0042 * 100, abs=0.5
        )
        # route_intent has no graduation.
        route = by_name["route_intent"]
        assert route.graduation_event is None
        assert route.first_phase == "RULE"
        assert route.latest_phase == "RULE"


