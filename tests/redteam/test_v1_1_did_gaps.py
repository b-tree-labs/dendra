@pytest.mark.xfail(
    reason=(
        "Queued for v1.2: ``follow_symlinks=False`` on Path.glob / "
        "Path.iterdir requires Python 3.13. Dendra's ``requires-python`` "
        "is currently >=3.10; the resolved-under-root check in cmd_refresh "
        "already catches the symlink-escape attack at a coarser layer "
        "(see test_refresh_walk_does_not_follow_symlink_to_outside)."
    ),
    strict=True,
)
def test_refresh_walk_uses_follow_symlinks_false() -> None:
    """Pin the post-fix contract: every Path.rglob / Path.iterdir call
    in cmd_refresh passes ``follow_symlinks=False``. The current
    code does not (Python 3.10 doesn't support that kwarg), so this
    test xfails until the min Python bump.
    """
    import inspect

    from dendra import cli

    src = inspect.getsource(cli.cmd_refresh)
    # Both walk calls must pass follow_symlinks=False once we land on 3.13.
    assert src.count("follow_symlinks=False") >= 1


