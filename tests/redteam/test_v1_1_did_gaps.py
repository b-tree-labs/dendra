# Gap 2: whoami ANSI / control-char stripping
# ---------------------------------------------------------------------


class TestWhoamiAnsiStripping:
    """A hostile API key with embedded ANSI ESC sequences (or other
    control chars) must NOT render terminal-control bytes when
    ``dendra whoami`` prints the truncated key. The fix strips
    non-printable bytes before display; the underlying credential
    is unchanged.
    """

    def test_whoami_strips_ansi_escape_in_truncated_key(
        self, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]
    ) -> None:
        """A key containing ESC[ (ANSI CSI) must not surface raw bytes
        in stdout. Specifically: no 0x1b byte in the printed line.
        """
        from dendra.cli import cmd_whoami

        # Embed ESC + CSI sequence "set background red". Real terminals
        # would render it as a color flip; we want it stripped.
        hostile_key = "AAAAAAAA\x1b[41mBBBBBBBB"
        monkeypatch.setenv("DENDRA_API_KEY", hostile_key)
        rc = cmd_whoami(argparse.Namespace())
        out = capsys.readouterr().out
        assert rc == 0
        assert "\x1b" not in out, f"whoami leaked an ESC byte in the rendered output: {out!r}"

    def test_whoami_strips_other_control_chars(
        self, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]
    ) -> None:
        """BEL (\\x07), backspace (\\x08), VT (\\x0b), DEL (\\x7f) are
        all non-printable and should be stripped when surfacing the
        truncated key.
        """
        from dendra.cli import cmd_whoami

        hostile_key = "PREFIX_\x07\x08\x0b\x0c\x7f_SUFFIX_1234"
        monkeypatch.setenv("DENDRA_API_KEY", hostile_key)
        rc = cmd_whoami(argparse.Namespace())
        out = capsys.readouterr().out
        assert rc == 0
        for forbidden in ("\x07", "\x08", "\x0b", "\x0c", "\x7f"):
            assert forbidden not in out, (
                f"whoami leaked control byte {forbidden!r} in output: {out!r}"
            )

    def test_whoami_preserves_normal_printable_chars(
        self, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]
    ) -> None:
        """Sanitization must not destroy normal printable chars. Pin
        that an ordinary key still surfaces its first 8 + last 4 in
        the truncated form.
        """
        from dendra.cli import cmd_whoami

        monkeypatch.setenv("DENDRA_API_KEY", "abcdefghijklmnop1234")
        rc = cmd_whoami(argparse.Namespace())
        out = capsys.readouterr().out
        assert rc == 0
        assert "abcdefgh" in out
        assert "1234" in out
        assert "..." in out


