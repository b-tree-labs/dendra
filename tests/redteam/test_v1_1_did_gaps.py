# Gap 1: Header version / hash-length handling
# ---------------------------------------------------------------------


class TestHeaderHashLengthRange:
    """``parse_generated_header`` accepts a 32-hex-char blake2b-128 hash.
    A future Dendra version that ships a longer hash would otherwise
    quietly mismatch the regex and raise a generic ValueError; the
    fix names the version so operators know the file came from a
    newer Dendra.
    """

    def test_longer_hash_rejected_with_unsupported_version_message(self) -> None:
        """A 64-hex-char hash (e.g. blake2b-256) must reject with a
        message that names the unsupported header so the operator
        knows to upgrade Dendra rather than chasing a corrupt-file
        ghost.
        """
        from dendra import refresh as refresh_mod

        text = (
            "# Generated by Dendra v9.9.9.\n"
            "# Source: pkg.mod:fn\n"
            "# AST hash: " + ("a" * 64) + "\n"
            "# Content hash: " + ("b" * 64) + "\n"
            "body\n"
        )
        with pytest.raises(ValueError) as excinfo:
            refresh_mod.parse_generated_header(text)
        msg = str(excinfo.value)
        # The error must mention the version delta or "unsupported"
        # so the operator's first instinct is "upgrade Dendra", not
        # "the file is corrupt".
        assert "unsupported" in msg.lower() or "newer Dendra" in msg or "v9.9.9" in msg, (
            f"version-mismatch error should name the unsupported version; got: {msg!r}"
        )

    def test_zero_length_hash_rejected(self) -> None:
        """An empty hash field must reject (not silently pass-through)."""
        from dendra import refresh as refresh_mod

        text = (
            "# Generated by Dendra v1.0.0.\n"
            "# Source: pkg.mod:fn\n"
            "# AST hash: \n"  # empty after the colon
            "# Content hash: " + ("b" * 32) + "\n"
            "body\n"
        )
        with pytest.raises(ValueError):
            refresh_mod.parse_generated_header(text)

    def test_within_range_hash_still_accepted(self) -> None:
        """The current 32-hex-char blake2b-128 must continue to parse
        cleanly (regression guard against an over-tight range).
        """
        from dendra import refresh as refresh_mod

        text = (
            "# Generated by Dendra v1.0.0.\n"
            "# Source: pkg.mod:fn\n"
            "# AST hash: " + ("a" * 32) + "\n"
            "# Content hash: " + ("b" * 32) + "\n"
            "body\n"
        )
        h = refresh_mod.parse_generated_header(text)
        assert h.source_ast_hash == "a" * 32
        assert h.generated_content_hash == "b" * 32


