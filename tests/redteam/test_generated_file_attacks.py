def test_header_with_embedded_newline_in_source_module_rejected():
    """Embedded newline in the ``# Source:`` field should not let an
    attacker forge a second header line. The regex is line-anchored
    in spirit (\\w+ won't match newlines), so this rejects with
    ValueError on the malformed AST-hash line.

    BUG FIX (defense-in-depth): newline in source_module would otherwise
    let an attacker terminate the header and inject directives. The
    regex forbids it because ``[\\w.]+`` excludes newlines.
    """
    text = (
        "# Generated by Dendra v1.0.0.\n"
        "# Source: pkg.mod\nFAKE: spam:fn\n"
        "# AST hash: " + ("a" * 32) + "\n"
        "# Content hash: " + ("b" * 32) + "\n"
        "body\n"
    )
    with pytest.raises(ValueError):
        refresh_mod.parse_generated_header(text)


def test_header_with_truncated_hash_rejected():
    """Hash field shorter than 32 hex chars must be rejected, not
    silently treated as up-to-date.
    """
    text = (
        "# Generated by Dendra v1.0.0.\n"
        "# Source: pkg.mod:fn\n"
        "# AST hash: " + ("a" * 16) + "\n"  # truncated
        "# Content hash: " + ("b" * 32) + "\n"
        "body\n"
    )
    with pytest.raises(ValueError):
        refresh_mod.parse_generated_header(text)


def test_header_with_long_garbage_rejected():
    """A "header" that is just a 100KB blob of comment lines without
    the required structure must be rejected, not silently DOS'd.
    """
    text = "\n".join("# garbage " + str(i) for i in range(10_000))
    with pytest.raises(ValueError):
        refresh_mod.parse_generated_header(text)


def test_header_missing_prefix_rejected():
    """File without the canonical ``# Generated by Dendra`` prefix must
    be rejected, not silently parsed.
    """
    text = (
        "# Source: pkg.mod:fn\n# AST hash: " + ("a" * 32) + "\n# Content hash: " + ("b" * 32) + "\n"
    )
    with pytest.raises(ValueError):
        refresh_mod.parse_generated_header(text)


def test_header_with_unicode_in_module_name_rejected():
    """Module names are ``[\\w.]+`` ASCII; non-ASCII unicode that some
    normalizers fold to ASCII must NOT be accepted as a path-trickery
    vector.

    \\w in Python re is unicode by default, so e.g. "пкг.мод" might be
    accepted. We pin the current behavior so any tightening is a
    deliberate change rather than an accident.
    """
    text = (
        "# Generated by Dendra v1.0.0.\n"
        "# Source: пкг.мод:fn\n"
        "# AST hash: " + ("a" * 32) + "\n"
        "# Content hash: " + ("b" * 32) + "\n"
    )
    # Either accepted (module name flows through as-is, harmless)
    # or rejected. Never silently transformed.
    try:
        h = refresh_mod.parse_generated_header(text)
        assert h.source_module == "пкг.мод"
    except ValueError:
        pass


# ---------------------------------------------------------------------
# AST-hash collision threat model - documented, not exploited
# ---------------------------------------------------------------------


def test_ast_hash_is_blake2b_128_collision_resistant():
    """Threat model: AST-hash is blake2b-128. Collision-resistance is
    ~2^64 work (birthday) - far above the relevant attacker scope
    (single-developer machine, bounded source corpus).

    We don't need to demonstrate non-collision; we pin the algorithm so
    any future weakening to a shorter digest must be deliberate.
    """
    h = refresh_mod.ast_hash("def f(x): return x")
    assert len(h) == 32  # 16 bytes = 128 bits = 32 hex chars


def test_content_hash_catches_drift(tmp_path):
    """If an attacker forges a header with a stale AST hash but
    legitimately-shaped body, the CONTENT hash mismatch (USER_EDITED)
    triggers refusal in `dendra refresh`. This is the defense-in-depth.
    """
    src = tmp_path / "src.py"
    src.write_text("def fn(x):\n    return x\n")

    gen = tmp_path / "__dendra_generated__" / "src__fn.py"
    gen.parent.mkdir()
    refresh_mod.write_generated_file(
        gen,
        source_module="src",
        source_function="fn",
        source_ast_hash=refresh_mod.ast_hash("def fn(x):\n    return x\n"),
        content="# legitimate body\n",
        dendra_version="1.0.0",
    )
    # Tamper with the body without touching the header:
    text = gen.read_text()
    tampered = text + "\n# attacker edit\n"
    gen.write_text(tampered)

    status = refresh_mod.detect_drift(src, "fn", gen)
    assert status is refresh_mod.DriftStatus.USER_EDITED


