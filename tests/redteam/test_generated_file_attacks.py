@pytest.mark.skipif(sys.platform.startswith("win"), reason="symlinks need admin on Windows")
def test_refresh_walk_does_not_follow_symlink_to_outside(tmp_path, capsys):
    """A ``__dendra_generated__`` symlink pointing outside the project
    root must NOT be walked by ``dendra refresh``.

    BUG FIX: ``Path.rglob`` resolves entries through symlinks by default,
    so a malicious ``__dendra_generated__`` symlink inside the project
    root pointing at e.g. ``/etc`` would let the walker glob (and
    parse-read) arbitrary *.py files there. No code is executed
    (parse-only), but presenting an out-of-tree path leaks file
    existence. Fix: ``cmd_refresh`` now filters generated dirs whose
    ``.resolve()`` escapes the project root and emits a stderr warning.
    """
    import argparse

    from dendra.cli import cmd_refresh

    project = tmp_path / "project"
    project.mkdir()
    outside = tmp_path / "outside_target"
    outside.mkdir()
    (project / "__dendra_generated__").symlink_to(outside)
    (outside / "leaked.py").write_text(
        "# Generated by Dendra v1.0.0.\n"
        "# Source: leaked:fn\n"
        f"# AST hash: {'a' * 32}\n"
        f"# Content hash: {'b' * 32}\n"
        "body\n"
    )

    args = argparse.Namespace(path=str(project), check=False, force=False)
    rc = cmd_refresh(args)
    captured = capsys.readouterr()

    # Walker refused the escape; the leaked file was never inspected.
    assert "resolves outside project root" in captured.err
    # cmd_refresh treats the project as having no generated files.
    assert "No Dendra-generated files found" in captured.out or rc == 0


@pytest.mark.skipif(sys.platform.startswith("win"), reason="symlinks need admin on Windows")
def test_refresh_walk_legitimate_subdirs_still_work(tmp_path, capsys):
    """The symlink-escape filter must NOT break the normal case where
    ``__dendra_generated__`` is a real directory inside the project.
    """
    import argparse

    from dendra.cli import cmd_refresh

    project = tmp_path / "project"
    gen = project / "__dendra_generated__"
    gen.mkdir(parents=True)
    src = project / "src.py"
    src.write_text("def fn(x):\n    return x\n")
    refresh_mod.write_generated_file(
        gen / "src__fn.py",
        source_module="src",
        source_function="fn",
        source_ast_hash=refresh_mod.ast_hash("def fn(x):\n    return x\n"),
        content="# body\n",
        dendra_version="1.0.0",
    )

    args = argparse.Namespace(path=str(project), check=True, force=False)
    rc = cmd_refresh(args)
    captured = capsys.readouterr()
    # The file was found and inspected - that's the load-bearing assertion
    # for this test (the symlink filter didn't break the legit case).
    assert "Scanned 1 generated file" in captured.out
    # rc may be 0 (UP_TO_DATE) or 1 (drift detected - depends on
    # whether ast.unparse output matches the literal source byte-for-byte).
    assert rc in (0, 1)
    # Crucially: no "resolves outside project root" warning.
    assert "resolves outside" not in captured.err


@pytest.mark.skipif(sys.platform.startswith("win"), reason="symlinks need admin on Windows")
def test_individual_generated_file_symlink_pointing_outside(tmp_path):
    """A *file* inside __dendra_generated__/ that's a symlink to an
    arbitrary path. Reading it is a parser-only operation, so no exec,
    but pin the behavior: parse_generated_header on whatever the
    symlink points at must succeed-or-fail cleanly, not crash.
    """
    project = tmp_path / "project"
    gen = project / "__dendra_generated__"
    gen.mkdir(parents=True)
    target = tmp_path / "outside.py"
    target.write_text("def evil(): pass\n")
    (gen / "x__y.py").symlink_to(target)

    text = (gen / "x__y.py").read_text()
    # Not a Dendra-generated file → ValueError, not crash.
    with pytest.raises(ValueError):
        refresh_mod.parse_generated_header(text)


