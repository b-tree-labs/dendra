"""Generated-file (``__dendra_generated__/``) attack surface.

Threat model:
  - Generated files are emitted by ``dendra init --auto-lift`` and read
    by ``dendra refresh`` / ``dendra doctor``.
  - The reader must NOT execute the file body - only parse the header.
  - The CLI walk must NOT follow symlinks or descend into ``..`` paths
    above the project root.

We assert the parser is a parser, not an interpreter; we assert the
walker stays bounded; we document the residual threat model for
AST-hash collisions.
"""

from __future__ import annotations

import os
import sys

import pytest

from dendra import refresh as refresh_mod

pytestmark = pytest.mark.redteam


# ---------------------------------------------------------------------
# parse_generated_header is a parser, not an interpreter
# ---------------------------------------------------------------------


def test_parse_header_does_not_exec_file():
    """A generated file containing ``os.system(...)`` at module level
    must NEVER be executed by ``parse_generated_header``.

    The parser only consumes the ``#``-comment block at the top.
    """
    sentinel = "/tmp/dendra-generated-file-rce-fired"
    if os.path.exists(sentinel):
        os.unlink(sentinel)

    text = (
        "# Generated by Dendra v1.0.0.  DO NOT EDIT BY HAND.\n"
        "# Source: pkg.mod:fn\n"
        "# AST hash: " + ("a" * 32) + "\n"
        "# Content hash: " + ("b" * 32) + "\n"
        "# Run `dendra refresh`.\n"
        "import os\n"
        f"os.system('touch {sentinel}')\n"
    )

    header = refresh_mod.parse_generated_header(text)
    assert header.source_module == "pkg.mod"
    assert not os.path.exists(sentinel), (
        "parse_generated_header MUST NOT execute the body - that's a "
        "trivial RCE for any user who runs `dendra refresh` on a "
        "repo with a poisoned generated file."
    )


def test_detect_drift_does_not_exec_file(tmp_path):
    """detect_drift reads the generated file as text - never imports it."""
    sentinel = tmp_path / "would-have-fired"
    src = tmp_path / "src.py"
    src.write_text("def fn(x):\n    return x\n")
    gen = tmp_path / "__dendra_generated__" / "src__fn.py"
    gen.parent.mkdir()
    body = f"import os\nos.system('touch {sentinel}')\n"
    refresh_mod.write_generated_file(
        gen,
        source_module="src",
        source_function="fn",
        source_ast_hash="0" * 32,
        content=body,
        dendra_version="1.0.0",
    )
    # drift comes back USER_EDITED or whatever - the point is no exec.
    refresh_mod.detect_drift(src, "fn", gen)
    assert not sentinel.exists()


